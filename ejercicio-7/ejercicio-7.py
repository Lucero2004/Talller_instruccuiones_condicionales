# Ejercicio-7
# Programa para determinar si dados 3 numeros enteros,la suma de los 2 primeros es igual al tercero.

a=int(input("Ingerese el valor del primer numero: "))
b=int(input("Ingerese el valor del segundo numero: "))
c=int(input("Ingerese el valor del tercer numero: "))

if c==(a+b):
    print("La suma de los 2 primeros numeros es igual al ultimo numero digitado")
else:
    print("La suma de los 2 primeros numeros no son iguales al ultimo digitado")